#include <stdio.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int (i) = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}

int	main(int ac, char **av)
{
	if (ac == 3)
		printf("%s", ft_strcpy(av[1], av[2]));
	return (0);
}
